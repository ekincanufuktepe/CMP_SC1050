#include<stdio.h>

/* 
    This is a "block comment"
    I can write as long as I want 
    without worrying of the number of lines
*/
int main() {
    // this is a "line comment" and you can only have the comment for one line
    // if I want to continue with the comment you need another double forward slash
    printf("Hello, I am Ekin!\nNice to meet you all\n\tI hope you are enjoying this\n");
    return 0;
}